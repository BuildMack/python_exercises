food = 'Icecream'

print("Is Icecream == Icecream? Should be True.")
print(food == 'Icecream', end = '\n\n')

print("Is Icecream == icecream? Should be False.")
print(food == 'icecream', end = '\n\n')

print("Is food.lower() == icecream? Should be True.")
print(food.lower() == 'icecream', end = '\n\n')
